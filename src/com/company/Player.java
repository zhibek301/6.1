package com.company;

public class Player {
    private String  PlayerDamage;
    private String PlayerHealth;
    private String PlayerHeal;

    public String getPlayerDamage() {
        return PlayerDamage;
    }

    public void setPlayerDamage(String playerDamage) {
        PlayerDamage = playerDamage;
    }

    public String getPlayerHealth() {
        return PlayerHealth;
    }

    public void setPlayerHealth(String playerHealth) {
        PlayerHealth = playerHealth;
    }

    public String getPlayerHeal() {
        return PlayerHeal;
    }

    public void setPlayerHeal(String playerHeal) {
        PlayerHeal = playerHeal;
    }
}
