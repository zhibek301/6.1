package com.company;

public class Warrior extends Player {
    private String  WarriorDamage;
    private String WarriorHealth;
    private String WarriorHeal;

    public String getWarriorDamage() {
        return WarriorDamage;
    }

    public void setWarriorDamage(String warriorDamage) {
        WarriorDamage = warriorDamage;
    }

    public String getWarriorHealth() {
        return WarriorHealth;
    }

    public void setWarriorHealth(String warriorHealth) {
        WarriorHealth = warriorHealth;
    }

    public String getWarriorHeal() {
        return WarriorHeal;
    }

    public void setWarriorHeal(String warriorHeal) {
        WarriorHeal = warriorHeal;
    }
}
